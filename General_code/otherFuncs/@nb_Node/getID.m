function id = getID(obj)
% Syntax:
% 
% id = getID(obj)
% 
% Description:
% 
% Get the id of the object
% 
% Input:
% 
% - obj       : An object of class nb_Node
% 
% Output:
% 
% - id        : The id of the object. As a string.
% 
% Examples:
% 
% obj = obj.getID();
% 
% Written by Kenneth S. Paulsen

% Copyright (c) 2021, Kenneth Sæterhagen Paulsen

    id = obj.id;

end
