function importDistributionGUI(gui)
% Syntax:
%
% importDistributionGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Henrik Halvorsen Hortemo and Kenneth Sæterhagen Paulsen

% Copyright (c) 2023, Kenneth Sæterhagen Paulsen

    % Figure
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0, 'defaultUicontrolBackgroundColor');
    gui.figureHandle2 = figure(...
        'visible',        'off',...
        'units',          'characters',...
        'position',       [40   15  100   40],...
        'Color',          defaultBackground,...
        'name',           'Select distributions to be assign',...
        'numberTitle',    'off',...
        'menuBar',        'None',...
        'windowStyle',    'modal',...
        'toolBar',        'None',...
        'resize',         'on');            
    nb_moveFigureToMonitor(gui.figureHandle2, currentMonitor, 'center');

    % Loaction
    startX       = 0.04;
    listWidth    = (1 - startX*3)/2;
    startX2      = startX*2 + listWidth;
    spaceY       = 0.2;
    startLY      = 0.2;
    listHeight   = 1 - startLY - spaceY;
    textY        = 1 - spaceY;
    radioY       = 1 - spaceY/2;
    textHeight   = 0.04;
    buttonWidth  = 0.2;
    buttonHeight = 0.05;
    buttonX1     = 0.25 - buttonWidth/2;
    buttonX2     = 0.75 - buttonWidth/2;
    buttonY      = startLY/2 - buttonHeight/2;
    
    % Mean shift
    gui.meanShiftRadiobutton = uicontrol(...
              'units',                  'normalized',...
              'position',               [startX, radioY, listWidth, textHeight],...
              'parent',                 gui.figureHandle2,...
              'style',                  'radiobutton',...
              'horizontalAlignment',    'left',...
              'value',                  1,...
              'string',                 'Adjust mean');
    
    % Dates
    uicontrol(...
              'units',                  'normalized',...
              'position',               [startX, textY, listWidth, textHeight],...
              'parent',                 gui.figureHandle2,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Dates');

    gui.datesListBox = uicontrol(...
          'units',          'normalized',...
          'position',       [startX, startLY, listWidth, listHeight],...
          'parent',         gui.figureHandle2,...
          'background',     [1 1 1],...
          'style',          'listbox',...
          'string',         gui.dates,...
          'value',          1,...
          'max',            length(gui.dates));
    
    % Variable
    uicontrol(...
              'units',                  'normalized',...
              'position',               [startX2, textY, listWidth, textHeight],...
              'parent',                 gui.figureHandle2,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Variable');

    gui.variablesListBox = uicontrol(...
          'units',          'normalized',...
          'position',       [startX2, startLY, listWidth, listHeight],...
          'parent',         gui.figureHandle2,...
          'background',     [1 1 1],...
          'style',          'listbox',...
          'string',         gui.variables,...
          'value',          1,...
          'max',            length(gui.variables));  
      
    % Display distribution  
    uicontrol(...
          'units',          'normalized',...
          'position',       [buttonX1, buttonY, buttonWidth, buttonHeight],...
          'parent',         gui.figureHandle2,...
          'style',          'pushbutton',...
          'Interruptible',  'off',...
          'string',         'Display',...
          'callback',       @gui.displayCallback); 
      
    % Assing distribution  
    uicontrol(...
          'units',          'normalized',...
          'position',       [buttonX2, buttonY, buttonWidth, buttonHeight],...
          'parent',         gui.figureHandle2,...
          'style',          'pushbutton',...
          'Interruptible',  'off',...
          'string',         'Assing to',...
          'callback',       @gui.assignCallback);  
      
    % Make gui
    set(gui.figureHandle2,'visible','on')

end
