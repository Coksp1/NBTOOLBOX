function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Eyo Herstad

% Copyright (c) 2024, Kenneth SÃ¦terhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;
    figName = 'Interpolate variables';
    string  = 'Choose method';
    
    % Create window
    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': ' figName];
    else
        name = figName;
    end
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0,'defaultUicontrolBackgroundColor');
    f = figure('visible',        'off',...
               'units',          'characters',...
               'position',       [65   15  65   9],...
               'Color',          defaultBackground,...
               'name',           name,...
               'numberTitle',    'off',...
               'menuBar',        'None',...
               'toolBar',        'None',...
               'resize',         'off',...
               'windowStyle',    'modal');
    nb_moveFigureToMonitor(f,currentMonitor,'center');

    % Find the coordinate parameters
    startX          = 0.3;
    width           = 1 - startX*2;
    height          = 0.18;
    listExtraHeight = 0.04;

    uicontrol('units',              'normalized',...
              'position',           [startX, 0.65,width,height],...
              'parent',             f,...
              'style',              'text',...
              'string',             string,...
              'horizontalAlignment','left');             

    gui.popup = uicontrol(...
                     'units',             'normalized',...
                     'position',           [startX, 0.45,width,height + listExtraHeight],...
                     'parent',             f,...
                     'background',         [1 1 1],...
                     'style',              'popupmenu',...
                     'string',             {'Linear';'Nearest';'Spline';'Pchip';'Cubic';'V5Cubic'},...
                     'horizontalAlignment','right');         
        
    buttonWidth  = 0.3;
    buttonHeigth = 0.2;
    uicontrol('units',       'normalized',...
              'position',    [0.5 - buttonWidth/2, (0.45 - buttonHeigth)/2,buttonWidth,buttonHeigth],...
              'parent',      f,...
              'style',       'pushbutton',...
              'string',      'OK',...
              'callback',    @gui.interpolateCallback);

    % Make GUI visible
    set(f,'visible','on');

end
