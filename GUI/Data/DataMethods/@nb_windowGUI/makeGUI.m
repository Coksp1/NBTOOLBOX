function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2021, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;

    % Create window
    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': Window'];
    else
        name = 'Window';
    end
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0,'defaultUicontrolBackgroundColor');
    f = figure('visible',        'off',...
               'units',          'characters',...
               'position',       [65   15  50   17],...
               'Color',          defaultBackground,...
               'name',           name,...
               'numberTitle',    'off',...
               'menuBar',        'None',...
               'toolBar',        'None',...
               'resize',         'off',...
               'windowStyle',    'modal');
    nb_moveFigureToMonitor(f,currentMonitor,'center');
    
    if isa(gui.data,'nb_ts')
        list1 = nb_getDatesAndLocals(gui.data,0);
        list2 = nb_getDatesAndLocals(gui.data,1);
    else
        list1 = observations(gui.data,'cellstr');
        list2 = flipud(list1);  
    end
    list1 = [list1;{''}];
    list2 = [list2;{''}];
    
    % Find the coordinate parameters
    startX          = 0.14;
    width           = 1 - startX*2;
    height          = 0.09;
    listExtraHeight = 0.02;

    uicontrol('units',              'normalized',...
              'position',           [startX, 0.77,width,height],...
              'parent',             f,...
              'style',              'text',...
              'string',             'Select new start obs',...
              'horizontalAlignment','left');             

    gui.popupmenu1 = uicontrol(...
                     'units',              'normalized',...
                     'position',           [startX, 0.65,width,height + listExtraHeight],...
                     'parent',             f,...
                     'background',         [1 1 1],...
                     'style',              'popupmenu',...
                     'string',             list1,...
                     'horizontalAlignment','left');         

    uicontrol('units',              'normalized',...
              'position',           [startX, 0.45,width,height],...
              'parent',             f,...
              'style',              'text',...
              'string',             'Select new end obs',...
              'horizontalAlignment','left');             
 
    gui.popupmenu2 = uicontrol(...
                       'units',              'normalized',...
                       'position',           [startX, 0.34,width,height + listExtraHeight],...
                       'parent',             f,...
                       'background',         [1 1 1],...
                       'style',              'popupmenu',...
                       'string',             list2,...
                       'horizontalAlignment','left'); 

                
    buttonWidth  = 0.25;
    buttonHeigth = 0.13;
    uicontrol('units',              'normalized',...
              'position',           [0.5 - buttonWidth/2, (0.34 - buttonHeigth)/2,buttonWidth,buttonHeigth],...
              'parent',             f,...
              'style',              'pushbutton',...
              'Interruptible',      'off',...
              'busyAction',         'cancel',...
              'string',             'OK',...
              'callback',           @gui.windowCallback);

    % Make GUI visible
    set(f,'visible','on');

end
