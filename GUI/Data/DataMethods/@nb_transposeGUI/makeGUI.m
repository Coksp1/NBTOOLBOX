function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2021, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;

    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': Transpose'];
    else
        name = 'Transpose';
    end
    
    % Create window
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0,'defaultUicontrolBackgroundColor');
    f = figure('visible',        'off',...
               'units',          'characters',...
               'position',       [65   15  70   8.5],...
               'Color',          defaultBackground,...
               'name',           name,...
               'numberTitle',    'off',...
               'menuBar',        'None',...
               'toolBar',        'None',...
               'resize',         'off',...
               'windowStyle',    'modal');
    nb_moveFigureToMonitor(f,currentMonitor,'center');
          
    % Pushbutton
    %--------------------------------------------------------------
    buttonWidth  = 0.25;
    buttonHeigth = 0.26;
    uicontrol(...
        'units',            'normalized',...
        'position',         [0.5 - buttonWidth/2, 0.50 - buttonHeigth/2,buttonWidth,buttonHeigth],...
        'parent',           f,...
        'style',            'pushbutton',...
        'Interruptible',    'off',...
        'busyAction',       'cancel',...
        'string',           'Transpose',...
        'callback',         @gui.transposeCallback);

    % Make GUI visible
    set(f,'visible','on');

end
