function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2023, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;
    figName = 'Easter dummy';
    
    % Create window
    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': ' figName];
    else
        name = figName;
    end
    f = nb_guiFigure(mainGUI,name,[65, 15, 40, 10],'modal','off');
    
    % Locations
    %--------------------------------------------------------------
    numOfRows       = 3;
    marginY         = 0.15;
    rowHeight       = (1 - marginY*(numOfRows-1)) / numOfRows;
    spaceX          = 0.04;
    
    % Container panel
    container = uipanel(f,...
        'BorderWidth', 0,...
        'position', [0,marginY,1,1 - 2*marginY]);
    
    % Start date
    uicontrol(...
              'units',                  'normalized',...
              'position',               [spaceX, 2*(rowHeight + marginY), 0.7, rowHeight],...
              'parent',                 container,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Greedy');
    
    gui.components.greedy = uicontrol(...
              'units',                  'normalized',...
              'position',               [0.7, 2*(rowHeight + marginY), 0.3 - spaceX, rowHeight],...
              'parent',                 container,...
              'style',                  'radiobutton',...
              'horizontalAlignment',    'left',...
              'Interruptible',          'off',...
              'value',                  true);
   
    % Calculate button
    %--------------------------------------------------------------
    uicontrol(...
              'units',              'normalized',...
              'position',           [spaceX*2, 0, 1-spaceX*4, rowHeight],...
              'parent',             container,...
              'style',              'pushbutton',...
              'Interruptible',      'off',...
              'busyAction',         'cancel',...
              'string',             'OK',...
              'callback',           {@gui.calculateCallback, f});   

    % Make GUI visible
    set(f,'visible','on');

end
