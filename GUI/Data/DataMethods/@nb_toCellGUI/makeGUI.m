function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2023, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;

    % Decide the window name 
    figName = 'Convert to cell object';

    % Create window
    f = nb_guiFigure(mainGUI,figName,[65,15,60,10],'modal','off');

    % Locations
    %--------------------------------------------------------------
    startPopX = 0.3;
    widthPop  = 0.6;
    heightPop = 0.11;
    startTX   = 0.1;
    widthT    = widthPop - startTX*2;
    heightT   = 0.1;
    kk        = 4; 
    spaceYPop = (1 - heightPop*kk)/(kk + 1);
    extra     = -(heightPop - heightT);
        
    % Strip 
    %-------------------------------------------------------------
    if ~isa(gui.data,'nb_cs')
    uicontrol(...
              'units',                  'normalized',...
              'position',               [startTX, heightPop*(kk-1) + spaceYPop*kk + extra, widthT, heightT],...
              'parent',                 f,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Strip');

    gui.strip = uicontrol(...
              'units',              'normalized',...
              'position',           [startPopX, heightPop*(kk-1) + spaceYPop*kk, widthPop, heightPop],...
              'parent',             f,...
              'background',         [1 1 1],...
              'style',              'popupmenu',...
              'horizontalAlignment','left',...
              'Interruptible',      'off',...
              'value',              1,...
              'string',             {'On','Off'});    
    end
    
    % Format
    %--------------------------------------------------------------
    if isa(gui.data,'nb_ts')
        kk = kk - 1;
        uicontrol(...
                  'units',                  'normalized',...
                  'position',               [startTX, heightPop*(kk-1) + spaceYPop*kk + extra, widthT, heightT],...
                  'parent',                 f,...
                  'style',                  'text',...
                  'horizontalAlignment',    'left',...
                  'string',                 'Format');

        gui.format = uicontrol(...
                  'units',              'normalized',...
                  'position',           [startPopX, heightPop*(kk-1) + spaceYPop*kk, widthPop, heightPop],...
                  'parent',             f,...
                  'background',         [1 1 1],...
                  'style',              'popupmenu',...
                  'horizontalAlignment','left',...
                  'Interruptible',      'off',...
                  'string',             {'Default','fame','NBEnglish','NBNorwegian','xls'});   
    end
    
    % Calculate button
    %--------------------------------------------------------------
    buttonHeight = 0.18;
    buttonWidth  = 0.35;
    buttonXLoc   = 0.5 - buttonWidth/2;
    buttonYLoc   = (heightPop*(kk-1) + spaceYPop*kk)/2 - buttonHeight/2;
    
    uicontrol(...
              'units',              'normalized',...
              'position',           [buttonXLoc, buttonYLoc, buttonWidth, buttonHeight],...
              'parent',             f,...
              'style',              'pushbutton',...
              'Interruptible',      'off',...
              'busyAction',         'cancel',...
              'string',             'OK',...
              'callback',           @gui.convertCallback);   

    % Make GUI visible
    set(f,'visible','on');

end
