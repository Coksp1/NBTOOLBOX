function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2023, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;

    % Create window
    figName = 'Split Series';
    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': ' figName];
    else
        name = figName;
    end
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0,'defaultUicontrolBackgroundColor');
    f = figure('visible',        'off',...
               'units',          'characters',...
               'position',       [65,   15,  90,   30],...
               'Color',          defaultBackground,...
               'name',           name,...
               'numberTitle',    'off',...
               'menuBar',        'None',...
               'toolBar',        'None',...
               'resize',         'off',...
               'windowStyle',    'modal');
    nb_moveFigureToMonitor(f,currentMonitor,'center');

    % Create panels
    %--------------------------------------------------------------
    pSpace  = 0.02;
    pWidth1 = 0.55;
    pWidth2 = 1 - pWidth1 - pSpace*3;
    uip1 = uipanel(...
        'units',              'normalized',...
        'position',           [pSpace, pSpace, pWidth1, 1 - pSpace*2],...
        'parent',             f,...
        'title',              'Options');
    
    
    uip2 = uipanel(...
        'units',              'normalized',...
        'position',           [pSpace*2 + pWidth1, pSpace, pWidth2, 1 - pSpace*2],...
        'parent',             f,...
        'title',              'Select Variables');
    
    % Variables selection list
    %--------------------------------------------------------------
    gui.variables = uicontrol(...
      'units',              'normalized',...
      'position',           [0.02, 0.02, 0.96, 0.96],...
      'parent',             uip2,...
      'background',         [1 1 1],...
      'style',              'listbox',...
      'string',             gui.data.variables,...
      'max',                length(gui.data.variables),...
      'value',              1);
    
    % Locations
    %--------------------------------------------------------------
    startPopX = 0.5;
    widthPop  = 0.45;
    heightPop = 0.06;
    startTX   = 0.06;
    widthT    = 1- widthPop - startTX*2;
    heightT   = 0.0533;
%     widthB    = 1 - startPopX - widthPop - startTX*2;
%     startB    = startPopX + widthPop + startTX;
    kk        = 9; 
    spaceYPop = (1 - heightPop*kk)/(kk + 1);
    extra     = -(heightPop - heightT);
              
    % Split date
    %--------------------------------------------------------------
    uicontrol(...
              'units',                  'normalized',...
              'position',               [startTX, heightPop*(kk-1) + spaceYPop*kk + extra, widthT, heightT],...
              'parent',                 uip1,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Split date:');

    gui.dates = uicontrol(...
              'units',              'normalized',...
              'position',           [startPopX, heightPop*(kk-1) + spaceYPop*kk, widthPop, heightPop],...
              'parent',             uip1,...
              'background',         [1 1 1],...
              'style',              'popupmenu',...
              'horizontalAlignment','left',...
              'Interruptible',      'off',...
              'string',             nb_getDatesAndLocals(gui.data));               
     
    % Postfix
    %--------------------------------------------------------------
    kk = kk - 1;
    uicontrol(...
              'units',                  'normalized',...
              'position',               [startTX, heightPop*(kk-1) + spaceYPop*kk + extra, widthT, heightT],...
              'parent',                 uip1,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Postfix:');

    gui.postfix = uicontrol(...
              'units',              'normalized',...
              'position',           [startPopX, heightPop*(kk-1) + spaceYPop*kk, widthPop, heightPop],...
              'parent',             uip1,...
              'background',         [1 1 1],...
              'style',              'edit',...
              'horizontalAlignment','left',...
              'Interruptible',      'off',...
              'string',             '_split');        
          
    % Overlapping
    %--------------------------------------------------------------
    kk = kk - 1;
    uicontrol(...
              'units',                  'normalized',...
              'position',               [startTX, heightPop*(kk-1) + spaceYPop*kk + extra, widthT, heightT],...
              'parent',                 uip1,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Overlapping:');

    gui.overlapping = uicontrol(...
              'units',              'normalized',...
              'position',           [startPopX, heightPop*(kk-1) + spaceYPop*kk, widthPop, heightPop],...
              'parent',             uip1,...
              'style',              'radiobutton',...
              'horizontalAlignment','left',...
              'Interruptible',      'off',...
              'string',             '');       
          
    % Calculate button
    %--------------------------------------------------------------
    buttonHeight = 0.0667;
    buttonWidth  = 0.3;
    buttonXLoc   = 0.5 - buttonWidth/2;
    buttonYLoc   = (heightPop*(kk-1) + spaceYPop*kk)/2 - buttonHeight/2;
    
    uicontrol(...
              'units',              'normalized',...
              'position',           [buttonXLoc, buttonYLoc, buttonWidth, buttonHeight],...
              'parent',             uip1,...
              'style',              'pushbutton',...
              'Interruptible',      'off',...
              'busyAction',         'cancel',...
              'string',             'OK',...
              'callback',           @gui.splitCallback);   

    % Make GUI visible
    set(f,'visible','on');

end
