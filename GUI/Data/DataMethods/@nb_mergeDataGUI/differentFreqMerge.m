function differentFreqMerge(gui)
% Syntax:
%
% differentFreqMerge(gui)
%
% Description:
%
% Part of DAG. Make the user select the dataset to convert 
% 
% Written by Kenneth Sæterhagen Paulsen   

% Copyright (c) 2021, Kenneth Sæterhagen Paulsen

    name              = [gui.parent.guiName ': Select Dataset to Convert'];
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0,'defaultUicontrolBackgroundColor');
    f = figure('visible',        'off',...
               'units',          'characters',...
               'position',       [65   15  70   15],...
               'Color',          defaultBackground,...
               'name',           name,...
               'numberTitle',    'off',...
               'menuBar',        'None',...
               'toolBar',        'None',...
               'resize',         'off',...
               'windowStyle',    'modal');
    nb_moveFigureToMonitor(f,currentMonitor,'center');

    xSpace  = 0.15;
    widthT  = 1 - xSpace*2;
    heightT = 0.14;
    height  = 0.18;

    uicontrol('units',                  'normalized',...
              'position',               [xSpace, 0.76, widthT, heightT],...
              'parent',                 f,...
              'background',             defaultBackground,...
              'style',                  'text',...
              'string',                 'Select Dataset',...
              'horizontalAlignment',    'left'); 


    datasets = cellstr(char(gui.saveName1,gui.saveName2));      
    list = uicontrol('units',       'normalized',...
                     'position',    [xSpace, 0.6, widthT, height],...
                     'parent',      f,...
                     'background',  [1 1 1],...
                     'style',       'popupmenu',...
                     'string',      datasets);  

    gui.convertListBox = list;             
                 
    widthB  = 0.3;
    heightB = 0.14;                   
    uicontrol('units',       'normalized',...
              'position',    [(1 - widthB)/2, (0.6 - heightB)/2, widthB, heightB],...
              'parent',      f,...
              'string',      'Select',...
              'style',       'pushbutton',...
              'callback',    @gui.selectedObjectToConvert);              


    set(f,'visible','on');

end
