function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2023, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;

    % Create window
    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': Fill NaN Values'];
    else
        name = 'Fill NaN Values';
    end
    f = nb_guiFigure(mainGUI,name,[65   15  50   17],'modal','off');

    % Find the coordinate parameters
    startX          = 0.14;
    width           = 1 - startX*2;
    height          = 0.09;
    listExtraHeight = 0.02;

    gui.components.text1 = uicontrol(...
              'units',              'normalized',...
              'position',           [startX, 0.77,width,height],...
              'parent',             f,...
              'style',              'text',...
              'string',             'Periods from today',...
              'horizontalAlignment','left');             

    gui.components.edit1 = uicontrol(...
                     'units',              'normalized',...
                     'position',           [startX, 0.65,width,height + listExtraHeight],...
                     'parent',             f,...
                     'background',         [1 1 1],...
                     'style',              'edit',...
                     'string',             '',...
                     'horizontalAlignment','left');         

    gui.components.radio1 = uicontrol(...
              'units',              'normalized',...
              'position',           [startX, 0.45,width,height],...
              'parent',             f,...
              'style',              'radiobutton',...
              'string',             'Periods/Date',...
              'horizontalAlignment','left',...
              'value',              1,...
              'callback',           @gui.periodDateCallback);             
             
    buttonWidth  = 0.25;
    buttonHeigth = 0.13;
    uicontrol('units',              'normalized',...
              'position',           [0.5 - buttonWidth/2, (0.34 - buttonHeigth)/2,buttonWidth,buttonHeigth],...
              'parent',             f,...
              'style',              'pushbutton',...
              'Interruptible',      'off',...
              'busyAction',         'cancel',...
              'string',             'OK',...
              'callback',           @gui.calculateCallback);

    % Make GUI visible
    set(f,'visible','on');

end
