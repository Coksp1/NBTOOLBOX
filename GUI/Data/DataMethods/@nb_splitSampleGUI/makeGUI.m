function makeGUI(gui)
% Syntax:
%
% makeGUI(gui)
%
% Description:
%
% Part of DAG.
% 
% Written by Kenneth Sæterhagen Paulsen

% Copyright (c) 2024, Kenneth Sæterhagen Paulsen

    % Get the handle to the main program
    mainGUI = gui.parent;
    
    figName = 'Split sample';
    
    
    % Create window
    if isa(mainGUI,'nb_GUI')
        name = [mainGUI.guiName ': ' figName];
    else
        name = figName;
    end
    
    currentMonitor    = nb_getCurrentMonitor();
    defaultBackground = get(0,'defaultUicontrolBackgroundColor');
    f = figure('visible',        'off',...
               'units',          'characters',...
               'position',       [65,   15,  40,   7],...
               'Color',          defaultBackground,...
               'name',           name,...
               'numberTitle',    'off',...
               'menuBar',        'None',...
               'toolBar',        'None',...
               'resize',         'off',...
               'windowStyle',    'modal');
    nb_moveFigureToMonitor(f,currentMonitor,'center');
    
    % Locations
    %--------------------------------------------------------------
    numOfRows       = 2;
    marginX         = 0.07;
    marginY         = 0.15;
    rowHeight       = (1 - marginY*(numOfRows-1)) / numOfRows;
    
    % Container panel
    p = uipanel(f,...
        'BorderWidth', 0,...
        'position', [marginX marginY (1 - 2*marginX) (1 - 2*marginY)]);
              
    % Postfix
    %--------------------------------------------------------------
    uicontrol(...
              'units',                  'normalized',...
              'position',               [0, rowHeight + marginY, .8, .8*rowHeight],...
              'parent',                 p,...
              'style',                  'text',...
              'horizontalAlignment',    'left',...
              'string',                 'Subsample length:');

    gui.subsampleLengthBox = uicontrol(...
              'units',              'normalized',...
              'position',           [.8, rowHeight + marginY, .2, rowHeight],...
              'parent',             p,...
              'background',         [1 1 1],...
              'style',              'edit',...
              'horizontalAlignment','left',...
              'Interruptible',      'off',...
              'string',             '8');               
     
    % Calculate button
    %--------------------------------------------------------------
    uicontrol(...
              'units',              'normalized',...
              'position',           [0, 0, 1, rowHeight],...
              'parent',             p,...
              'style',              'pushbutton',...
              'Interruptible',      'off',...
              'busyAction',         'cancel',...
              'string',             'OK',...
              'callback',           {@gui.splitSampleCallback, f});   

    % Make GUI visible
    set(f,'visible','on');

end
